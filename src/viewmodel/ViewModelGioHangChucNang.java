package viewmodel;

import java.math.BigDecimal;

/**
 *
 * @author Admin
 */
public class ViewModelGioHangChucNang {

    private String maSP;
    private String tenSP;
    private int soLuong;
    private BigDecimal donGia;

    public ViewModelGioHangChucNang(String maSP, String tenSP, int soLuong, BigDecimal donGia) {
        this.maSP = maSP;
        this.tenSP = tenSP;
        this.soLuong = soLuong;
        this.donGia = donGia;
    }

    public ViewModelGioHangChucNang() {
    }

    public String getMaSP() {
        return maSP;
    }

    public void setMaSP(String maSP) {
        this.maSP = maSP;
    }

    public String getTenSP() {
        return tenSP;
    }

    public void setTenSP(String tenSP) {
        this.tenSP = tenSP;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public BigDecimal getDonGia() {
        return donGia;
    }

    public void setDonGia(BigDecimal donGia) {
        this.donGia = donGia;
    }

    @Override
    public String toString() {
        return "ViewModelGioHangChucNang{" + "maSP=" + maSP + ", tenSP=" + tenSP + ", soLuong=" + soLuong + ", donGia=" + donGia + '}';
    }

}
